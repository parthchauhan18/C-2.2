//w.a.p to show vovel or constant using switch case.//
#include<stdio.h>
int main()
{
	char ch;
	printf("enter the alphabet ");
	scanf("%c",&ch);
	switch(ch)
	{
		case 'A':
		printf("this is vovel");
		break;
		
		case 'E':
		printf("this is vovel");
		break;
		
		case 'I':
		printf("this is vovel");
		break;
		
		case 'O':
		printf("this is vovel");
		break;
		
		case 'U':
		printf("this is vovel");
		break;
		
		default:
		printf("this is constant");	
		break;
    }
		return 0;
			
		
}
